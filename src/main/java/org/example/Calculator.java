package org.example;

public class Calculator {

    public int summary(int a, int b){
        return a+b;
    }

    public int addition(int a, int b){
        return a*b;
    }

    public int division(int a, int b){
        return a/b;
    }

    public int minus(int a, int b){
        return a-b;
    }
}
